    private void jMI_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_GuardarActionPerformed
        try {
            FileWriter fw;
            PrintWriter pw;

            fw = new FileWriter("datos.txt");
            pw = new PrintWriter(fw);
            for (Object dato : cola.datos) {
                pw.print(dato);
            }

            pw.close();
            JOptionPane.showMessageDialog(null, "Datos Guardados");
            //abre el archivo despues de haber almacenado los datos
             Desktop.getDesktop().open(new File("datos.txt"));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Datos NO Guardados");
        }
    }//GEN-LAST:event_jMI_GuardarActionPerformed

