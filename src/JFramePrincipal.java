    private void jTF_DimKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_DimKeyTyped
        // esto es para que detecte solo numeros en la dimension
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }

        //al presionar enter el boton haga click
        char enter = evt.getKeyChar();
        if (enter == KeyEvent.VK_ENTER) {
            this.jButCrear.doClick();
        }
    }//GEN-LAST:event_jTF_DimKeyTyped

    private void jButCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCrearActionPerformed
        int dimension = Integer.parseInt(this.jTF_Dim.getText());
        cola = new ColaCircularT(dimension);
        modeloTabla.setColumnCount(dimension);

        //inicio headerchange
        //con esto se hace para que cuando el usuario digite la dimension 
        //de la cola, las cabeceras de la tabla autoincrementen
        modeloTabla.setColumnCount(dimension);

        //int cabecera = 0;//inicialmente comienza en cero para simular el vector donde se almacenan los datos de la cola 
        int cabecera = 1;//inicialice en 1 para que el usuario tenga una mejor comprension 
        JTableHeader head = jTableColas.getTableHeader();
        TableColumnModel tcm = head.getColumnModel();
        for (int i = 0; i < jTableColas.getColumnCount(); i++) {
            TableColumn tabCM = tcm.getColumn(i);
            tabCM.setHeaderValue(cabecera++);
        }
        jTableColas.repaint();
//fin headerchange

        //centrar los renglones
        DefaultTableCellRenderer renderA = new DefaultTableCellRenderer();
        renderA.setHorizontalAlignment(SwingConstants.CENTER);
        for (int i = 0; i < dimension; i++) {
            jTableColas.getColumnModel().getColumn(i).setCellRenderer(renderA);
        }


    }//GEN-LAST:event_jButCrearActionPerformed

    private void jButInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButInsertarActionPerformed

        try {
            if (!cola.esta_llena()) {
                if (this.jRB_Integer.isSelected()) {
                    int val = Integer.parseInt(this.jTFDato.getText());
                    cola.insertar(val, modeloTabla);
                    jTFDato.setText("");
                    jTFDato.requestFocus();
                } else if (this.jRB_String.isSelected()) {
                    String val = this.jTFDato.getText();
                    cola.insertar(val, modeloTabla);
                    jTFDato.setText("");
                    jTFDato.requestFocus();
                } else if (this.jRB_Character.isSelected()) {
                    char val = this.jTFDato.getText().charAt(0);
                    cola.insertar(val, modeloTabla);
                    jTFDato.setText("");
                    jTFDato.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "COLA LLENA - (Overflow)");
                jTFDato.setText("");
                jTFDato.requestFocus();
            }
        } catch (NumberFormatException e) {
            jTFDato.setText("Error");
            jTFDato.requestFocus();
        }
    }//GEN-LAST:event_jButInsertarActionPerformed

    private void jButEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButEliminarActionPerformed
        try {
            if (!cola.esta_vacia()) {
                cola.eliminar(modeloTabla, jTF_DE);
            } else {
                JOptionPane.showMessageDialog(null, "COLA VACIA - (Underflow)");
                jTFDato.setText("");
                jTFDato.requestFocus();
                this.jTF_DE.setText("");
            }
        } catch (NumberFormatException e) {
            jTFDato.setText("Error");
            jTFDato.requestFocus();
        }
    }//GEN-LAST:event_jButEliminarActionPerformed

